#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	sign;
	int	fnl;

	i = 0;
	sign = 1;
	fnl = 0;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t'
		|| str[i] == '\v' || str[i] == '\r' || str[i] == '\f')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		fnl = fnl * 10 + str[i] - '0';
		i++;
	}
	return (sign * fnl);
}
